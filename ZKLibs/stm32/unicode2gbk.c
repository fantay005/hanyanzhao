static uint16_t __binaryFindGBK(uint16_t unicode) {
	uint16_t tmp, gbk;
	uint32_t low = Bank1_NOR2_ADDR + UNICODE_TABLE_ADDR;
	uint32_t high = Bank1_NOR2_ADDR + UNICODE_TABLE_END_ADDR;
	uint32_t middle;
	short j = 0, k = 0;

	if ((unicode >= 0x4e00) && (unicode <= 0x9FA7)) {
		while (high > (low + 2)) {
			middle = (low + high) / 2;
			middle &= (~0x01);
			tmp = *((const uint16_t *)middle);
			if (unicode == tmp) {
				return *((const uint16_t *)(middle + GBK_TABLE_OFFSET_FROM_UNICODE));
			}

			if (unicode > tmp) {
				low = middle;
			} else {
				high = middle;
			}
		}
	} else {
	for (j, k; j < 1088; j++, k++) {
			if (unicode == ucs2_punctuation[j]) {
				gbk = 0x9000 + j;
				j = 1088;
			}
		}
		if (k == 1088) {
			gbk = 0xA1A1;
		}
		return gbk;
	}
	return 0xA1A1;
}

uint8_t *Unicode2GBK(const uint8_t *unicode, int len) {
	uint16_t code;
	uint8_t *ret, *p;

	p = ret = pvPortMalloc(len + 1);

	if (ret == NULL) {
		return NULL;
	}

	len = len & (~0x01);

	NorFlashMutexLock(portMAX_DELAY);

	for (; len > 0; len -= 2) {
		code = *unicode++;
		code += (*unicode++) << 8;

		if (code <= 0x007F) {
			*p++ = code;
		} else {
			code = __binaryFindGBK(code);
			*p++ = code >> 8;
			*p++ = code;
		}
	}
	NorFlashMutexUnlock();
	*p = 0;

	return ret;
}
